/**
 * add - compute the sum of two numbers
 * @left: left-sided number
 * @right: right-sidede number
 * Return: results
 */
int add(int left, int right)
{
	return (left + right);
}

/**
 * sub - compute the difference of two numbers
 * @left: lift-sided number
 * @right: right-sided number
 * Return: result
 */
int sub(int left, int right)
{
	return (left - right);
}

/**
 * mul - compute the product of two numbers
 * @left: integer
 * @right: integer
 * Return: result
 */
int mul(int left, int right)
{
	return (left * right);
}

/**
 * div - compute the division of two numbers
 * @left: integer
 * @right: integer
 * Return: result
 */
int div(int left, int right)
{
	return (left / right);
}

/**
 * mod - compute the modulus of two numbers
 * @left: integer
 * @right: integer
 * Return: results
 */
int mod(int left, int right)
{
	return (left % right);
}
